package week4_54question;

public class AreaAndPerimeterOfARectangle {
    public static void main(String[] args) {

// Question 31: Area and Perimeter of a Rectangle
//
// Create two double variables named length and width and assign them the values 5.0 and 3.0.
// Calculate the area and perimeter of the rectangle.
// Print the area and perimeter.

        double length = 5.0;
        double width = 3.0;
        double area = length * width; // area = length * width
        double perimeter = 2 * (length + width); // perimeter = 2 * (length + width)

        System.out.println("area: " + area);
        System.out.println("perimeter: " + perimeter);



    }
}
